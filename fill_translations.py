def fill_po_file(po_file_path, lang='en'):
    """Fill translations in .po file

    Important:
    - For Arabic .po file: msgid is the source text (can be Arabic or English), msgstr is Arabic translation
    - For English .po file: msgid is the source text (can be Arabic or English), msgstr is English translation

    Since our templates use Arabic text in {% trans %}, the msgid will be Arabic.
    - Arabic .po: msgid="التصاريح" msgstr="التصاريح" (keep same)
    - English .po: msgid="التصاريح" msgstr="Permits" (translate to English)
    """
    print(f"Processing {po_file_path}...")

    with open(po_file_path, 'r', encoding='utf-8') as f:
        content = f.read()

    # Find all msgid/msgstr pairs
    pattern = r'msgid "(.*?)"\nmsgstr "(.*?)"'

    def replace_translation(match):
        msgid = match.group(1)
        msgstr = match.group(2)

        # Skip empty msgid
        if not msgid or msgid == '':
            return match.group(0)

        # Skip if already has a different translation (not empty and not same as msgid)
        if msgstr and msgstr != '' and msgstr != msgid:
            return match.group(0)

        # For Arabic file, keep Arabic as is (msgid = msgstr)
        if lang == 'ar':
            return f'msgid "{msgid}"\nmsgstr "{msgid}"'

        # For English file, translate from Arabic to English
        if lang == 'en':
            if msgid in translations:
                return f'msgid "{msgid}"\nmsgstr "{translations[msgid]}"'
            else:
                # If no translation found, keep msgid as msgstr
                return f'msgid "{msgid}"\nmsgstr "{msgid}"'

        # Default: keep as is
        return match.group(0)

    # Replace all translations
    new_content = re.sub(pattern, replace_translation, content)

    # Write back
    with open(po_file_path, 'w', encoding='utf-8') as f:
        f.write(new_content)

    print(f"✅ Completed {po_file_path}")


